public function getAddCart(Request $request,$id)
    {
        $food= Food::find($id);
        $oldCart=Session::has('cart')? Session::get('cart'):null;
        $cart=new Cart($oldCart);
        $cart->add($food,$food->id);
        $request->session()->put('cart',$cart);
        return redirect()->back();
    }


